package service;

public class Staff {
	private int s_no;
	private String s_name;
	private String s_secno;
	private String s_graduateday;
	private int sc_no;
	private int r_no;
	
	public int getSc_no() {
		return sc_no;
	}

	public void setSc_no(int sc_no) {
		this.sc_no = sc_no;
	}

	public int getR_no() {
		return r_no;
	}

	public void setR_no(int r_no) {
		this.r_no = r_no;
	}

	public int getS_no() {
		return s_no;
	}
	
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
	
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_secno() {
		return s_secno;
	}
	public void setS_secno(String s_secno) {
		this.s_secno = s_secno;
	}
	public String getS_graduateday() {
		return s_graduateday;
	}
	public void setS_graduateday(String s_graduateday) {
		this.s_graduateday = s_graduateday;
	}
	
	
}
